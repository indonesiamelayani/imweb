            Semakin majunya perkembangan teknologi dan informasi dewasa ini, sektor pelayanan publik makin dituntut untuk mampu memberikan layanan yang tidak hanya sekedar memenuhi standar minimal tapi mulai melihat adanya inklusivitas sosial. Kata “pelayanan ramah HAM” mulai dikenalkan di wajah pelayanan pada tahun setelah tahun 2010-an. Hal ini diawali dengan Indonesia meratifikasi Konvensi PBB tentang Hak penyandang disabilitas dengan terbitnya Undang-Undang No 19 Tahun 2011. Saat itu belum banyak sector layanan publik yang mampu memberikan layanan yang ramah HAM bagi pengguna layanan yang berkebutuhan khusus.
