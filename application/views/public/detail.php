          </p>
          <p>
            Kedepan bahkan diharapkan adanya produk atau ouput layanan sudah bisa diberikan dalam versi Braille dan makin banyak kantor pemerintah yang mewajibkan aparaturnya untuk mengikuti pelatihan penggunaan bahasa isyarat. Selanjutnya pembangunan fasilitas umum di seluruh gedung-gedung perkantoran pemerintah juga harus mewajibkan adanya jalur khusus bagi pengguna kursi roda dan guiding block, tempat tunggu yang dilengkapi bacaan bagi masyarakat yang buta atau layar informasi yang menampilkan penggunaan bahasa isyarat dan fasilitas bantu lain yang aman, nyaman serta mudah diakses.
          </p>
          <p>
